-- =============================================================================
-- snowball-income — 자유게시판 글 종류(category): 자유 / 건의사항 / 공지
-- =============================================================================
--
-- 의도
-- ---------------------------------------------------------------------------
-- 자유게시판(kind='board') 글을 세 갈래로 나눠 목록에서 한눈에 구분한다.
--   - 'free'       : 자유 (기본값). 질문·고민·잡담·뉴스 등 일반 글.
--   - 'suggestion' : 건의사항. 서비스 개선 요청.
--   - 'notice'     : 공지. **운영자만 선택**한다(아래 "이번 범위에 없는 것" 참고).
--
-- 값은 **영어 슬러그로 저장하고 화면에만 한국어**를 쓴다(kind 의 'portfolio'/'board' 와 같은 규율).
-- 정렬·필터·i18n 이 붙어도 DB 값을 그대로 둘 수 있고, 카피 변경이 데이터 마이그레이션을 부르지 않는다.
--
-- kind 와의 관계
-- ---------------------------------------------------------------------------
-- category 는 **게시판 안의 하위 분류**이고 kind 는 **표면(갤러리/게시판)** 이다. 서로 직교한다.
-- 갤러리 글(kind='portfolio')은 이 값을 쓰지 않으며 전부 기본값 'free' 로 남는다(무의미·무해).
-- 그래서 CHECK 로 kind×category 조합을 강제하지 않는다 — 갤러리 저장 경로는 완전 무변경이다.
--
-- 이번 범위에 없는 것 (사용자 결정)
-- ---------------------------------------------------------------------------
--   - **RLS 정책은 건드리지 않는다.** "공지는 운영자만"은 **UI 수준 제한**이며 **서버측 강제가
--     아니다** — profiles.is_admin(20260725000000)과 동일 방침이다. 클라이언트가 값을 위조하면
--     DB 는 category='notice' 를 그대로 받는다. 실제 권한이 필요해지면 그때 정책/함수를 별도
--     마이그레이션으로 설계한다.
--   - 목록 정렬/필터(공지 상단 고정 등)와 그 전용 인덱스는 이번 범위가 아니다. 표시(배지)만 한다.
--     그래서 인덱스를 추가하지 않는다(쓰기 비용만 늘리는 미사용 인덱스 회피).
--
-- 하위 호환 (중요)
-- ---------------------------------------------------------------------------
--   - NOT NULL DEFAULT 'free' → 기존 행 전부 'free' 로 백필된다(모든 기존 글 = 자유).
--   - CHECK 로 세 값만 허용한다. anon/authenticated 클라이언트가 임의 값을 못 넣는다.
--   - 읽기는 이 마이그레이션 **실행 전에도 안전**하다: 목록/상세 조회가 category 를 select 목록에
--     넣었다가 42703(undefined_column)을 받으면 그 컬럼을 뺀 컬럼셋으로 **1회 재시도**하고,
--     이후 세션 동안 없는 것으로 기억한다(shared/lib/supabase/queries.ts). 읽는 쪽은 값이 없으면
--     'free' 로 본다. (없는 컬럼을 select 목록에 넣으면 PostgREST 가 조회 **전체**를 실패시킨다.)
--   - 쓰기도 안전하다: 클라이언트는 **값이 기본값과 다를 때만** category 키를 전송한다.
--     즉 마이그레이션 전이라도 '자유' 글 게시와 카테고리를 안 바꾼 수정은 그대로 성공한다
--     (건의사항/공지 게시만 컬럼이 생길 때까지 실패한다 — 무음이 아니라 게시 실패 배너로 보인다).
--
-- 멱등성 (재실행 안전)
-- ---------------------------------------------------------------------------
-- 컬럼은 IF NOT EXISTS, comment 는 덮어쓰기, GRANT 는 additive 라 재실행해도 안전하다.
--
-- ⚠ 원장(append-only): 이전 마이그레이션(20260714… ~ 20260725…)은 절대 수정하지 않는다.
--   이 파일이 그 위에 category 를 덧붙인다.
-- ⚠ shared/lib/supabase/types.ts 와 동기화된다(PostRow.category, posts Insert/Update 의 category).
-- =============================================================================

-- =============================================================================
-- 1. category 컬럼 — 게시판 글 분류
-- =============================================================================
-- NOT NULL DEFAULT 'free': 기존 행 자동 백필(동작 불변). 인라인 CHECK 로 세 값만 허용.
alter table public.posts
  add column if not exists category text not null default 'free'
  check (category in ('free', 'suggestion', 'notice'));

comment on column public.posts.category is
  '자유게시판 글 분류: free(자유·기본) / suggestion(건의사항) / notice(공지). 화면 라벨은 한국어, DB 는 영어 슬러그. 갤러리(kind=''portfolio'') 글은 이 값을 쓰지 않고 기본값으로 남는다. ⚠ ''notice'' 제한은 UI 표시 수준일 뿐 RLS 로 강제되지 않는다.';

-- =============================================================================
-- 2. GRANT — category 컬럼 insert/update 권한 (additive)
-- =============================================================================
-- ⚠ insert GRANT 를 빠뜨리면 자유 이외의 분류로 게시할 수 없다(컬럼 GRANT 가 1차 관문).
grant insert (category) on public.posts to authenticated;

-- update 도 준다 — kind 와 다른 판단이다.
--   · kind 는 글이 **어느 목록에 사는가**라서 사후 변경이 곧 표면 이동(포트폴리오 글을 게시판으로
--     둔갑)이라 막았다.
--   · category 는 같은 목록 안의 **분류 실수**다. 잘못 고른 뒤 되돌릴 방법이 "글 삭제 후 재작성"
--     뿐이면 좋아요·댓글·조회수가 함께 날아간다. 그래서 작성자가 수정 화면에서 고칠 수 있게 한다.
-- 대상 행은 기존 posts UPDATE RLS 정책(본인 글만)이 그대로 제한한다 — 남의 글은 못 고친다.
grant update (category) on public.posts to authenticated;

-- =============================================================================
-- 끝. 요약
-- =============================================================================
--   - posts.category (text, NOT NULL DEFAULT 'free', CHECK in ('free','suggestion','notice')) 추가
--   - 기존 행은 'free' 로 백필 → 동작 불변(하위호환)
--   - insert(category) + update(category) GRANT(authenticated) — 분류 수정 허용
--   - RLS 정책 변경 없음. "공지는 운영자만"은 **UI 표시 힌트**이며 서버측 강제가 아니다
--   - 인덱스 추가 없음(정렬/필터는 이번 범위 밖)
