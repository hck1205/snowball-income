-- =============================================================================
--
-- 의도
-- ---------------------------------------------------------------------------
-- posts 테이블(=커뮤니티 게시글)에 `kind` 를 더해 두 갈래를 한 테이블에서 다룬다:
--   - 'portfolio' : 갤러리(포트폴리오/시나리오 공유글). 기존 글 전부가 여기 속한다.
--   - 'board'     : 자유게시판 글(질문·고민·잡담·건의·뉴스·의견). 본문 위주,
--                   시나리오 첨부는 선택. (payload/body 하이브리드 규칙은 그대로.)
--
-- 갤러리 격리
-- ---------------------------------------------------------------------------
-- 갤러리 조회(fetchGalleryPage)는 이제 kind='portfolio' 만 본다. 자유게시판 글이
-- 갤러리로 새지 않게 하는 것이 목적이다. 게시판 조회(fetchBoardPage)는 kind='board'.
--
-- 하위 호환 (중요)
-- ---------------------------------------------------------------------------
--   - kind 는 NOT NULL DEFAULT 'portfolio' 다. 기존 행은 전부 'portfolio' 로 백필돼
--     갤러리 동작이 **불변**이다(자유게시판이 생기기 전의 글 = 포트폴리오 공유글).
--   - CHECK 로 두 값만 허용한다. 클라이언트(anon 키)가 임의 값을 밀어 넣지 못한다.
--   - insert GRANT 만 준다(update 없음) → kind 는 생성 시점에 고정, 사후 변경 불가.
--
-- 멱등성 (재실행 안전)
-- ---------------------------------------------------------------------------
-- 컬럼은 IF NOT EXISTS, 인덱스는 IF NOT EXISTS. GRANT 는 additive(재실행 무해).
--
-- ⚠ 원장(append-only): 이전 마이그레이션(20260714… ~ 20260723…)은 절대 수정하지 않는다.
--   이 파일이 그 위에 kind 를 덧붙인다.
-- ⚠ shared/lib/supabase/types.ts 와 동기화된다(PostRow.kind, posts Insert 의 kind).
-- ⚠ 배포 순서: **이 마이그레이션을 먼저 실행**한 뒤, kind 를 쓰는 새 클라이언트를 배포한다.
--   순서가 뒤집혀 구 스키마에 신 클라이언트가 붙으면 insert(kind) 가 42703(undefined_column)
--   또는 GRANT 부재로 실패한다. (읽기 경로는 kind 를 select 하므로 컬럼 부재 시 목록도 깨진다.)
-- =============================================================================

-- =============================================================================
-- 1. kind 컬럼 — 글 종류
-- =============================================================================
-- NOT NULL DEFAULT 'portfolio': 기존 행 자동 백필(갤러리 불변). 인라인 CHECK 로 두 값만 허용.
alter table public.posts
  add column if not exists kind text not null default 'portfolio'
  check (kind in ('portfolio', 'board'));

-- =============================================================================
-- 2. GRANT — kind 컬럼 insert 권한 (additive)
-- =============================================================================
-- ⚠ 이걸 빠뜨리면 클라이언트가 kind='board' 로 게시할 수 없다(컬럼 GRANT 가 1차 관문).
-- update 는 주지 않는다 → 게시 후 kind 변경 불가(포트폴리오 글을 게시판으로 둔갑 금지).
-- 기존 GRANT(title/description/payload/body/is_public)는 그대로 두고 kind 만 더한다.
grant insert (kind) on public.posts to authenticated;

-- =============================================================================
-- 3. 자유게시판 목록 인덱스 — 공개 게시판 글 최신순 keyset
-- =============================================================================
-- 게시판 목록은 is_public=true AND kind='board' 를 (created_at desc, id desc) 로 훑는다.
-- 부분 인덱스(where kind='board' and is_public)로 게시판 글만 좁혀 스캔한다.
-- (기존 posts_public_recent_idx 는 kind 무관 전체 공개글이라 게시판만 특화하진 않는다.)
create index if not exists posts_board_recent_idx
  on public.posts (created_at desc, id desc)
  where is_public and kind = 'board';

-- =============================================================================
-- 끝. 요약
-- =============================================================================
