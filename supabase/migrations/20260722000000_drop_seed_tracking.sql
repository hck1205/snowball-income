-- 배경
-- ---------------------------------------------------------------------------
-- 커뮤니티 콜드스타트를 합성 페르소나로 시딩하려던 전략을 폐기하고, 운영자가 직접
-- 포트폴리오를 구성·공유·글 작성하는 **투명한** 방향으로 전환했다(2026-07-19, 사용자 확정).
-- 그에 따라 20260721000000_seed_tracking.sql 이 추가한 시드 추적 장치를 전부 되돌린다.
--
-- 원장(append-only) 원칙: 원본 마이그레이션(20260721…)은 남기고, 이 파일이 그 위에서
-- drop 으로 되돌린다. 모든 문장이 IF EXISTS 라 원본 미적용 DB 에서도 안전한 no-op 이다
-- (재실행 안전 / 멱등).
--
-- ⚠ 실행 주체: DROP 은 테이블 소유자(postgres) 권한이 필요하다. **service_role 키로는
--    이 테이블에 접근이 막혀 있으므로**(실측 확인) 반드시 Supabase SQL 에디터(postgres)
--    또는 `supabase db push` 로 실행한다.
-- ⚠ 하위호환: 앱 코드는 is_seed 를 읽지 않으므로(런타임 무영향), 컬럼/함수 제거는 안전하다.
--    is_seed 로 태깅된 시드 계정/글은 프로덕션에 생성된 적이 없다(시드 미실행 — 부분 시도
--    계정 1건은 이미 admin API 로 삭제 완료).
-- =============================================================================

-- 1) de-seed 운영 함수 제거 (service_role 전용이었음)
drop function if exists public.deseed_oldest_seed_scenarios(integer);

-- 2) 부분 인덱스 제거 (컬럼 드롭이 자동 제거하지만 순서를 명시)
drop index if exists public.profiles_is_seed_idx;

-- 3) is_seed 컬럼 제거
--    (원본이 걸어둔 컬럼 GRANT/REVOKE 는 컬럼과 함께 사라진다 — 별도 정리 불필요)
alter table public.profiles
  drop column if exists is_seed;

