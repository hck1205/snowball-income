-- 의도
-- ---------------------------------------------------------------------------
-- "이 사용자가 운영자인가"를 DB 한 곳에서 판정할 수 있게 profiles 에 불리언을 더한다.
-- 현재 소비처는 **UI 노출 제어 하나뿐**이다(관리자에게만 보이는 요소).
--
-- 이번 범위에 없는 것 (사용자 결정)
-- ---------------------------------------------------------------------------
--   - **RLS 정책은 건드리지 않는다.** 이번 차단은 UI 수준에서만 한다. 새 정책 추가·기존 정책
--     수정 없음. 즉 is_admin 은 **권한 부여가 아니라 표시 힌트**다 — 이 값으로 보호되는
--     서버측 권한은 아직 없고, 클라이언트가 값을 위조해도 DB 가 허용하는 범위는 그대로다.
--     (실제 권한 상승이 필요해지면 그때 정책/함수를 별도 마이그레이션으로 설계한다.)
--   - update GRANT 도 주지 않는다 → 사용자가 스스로를 관리자로 승격할 수 없다.
--     관리자 지정은 **대시보드/SQL 에디터에서 수동**으로만 한다:
--       update public.profiles set is_admin = true where id = '<auth.users.id>';
--
-- 하위 호환 (중요)
-- ---------------------------------------------------------------------------
--   - NOT NULL DEFAULT false → 기존 행 전부 false 로 백필된다(모두 일반 사용자).
--   - 읽기는 테이블 레벨 `grant select on public.profiles to anon, authenticated`
--     (20260714000000_community.sql:706)가 이후 추가 컬럼을 자동 포함하므로 **컬럼 GRANT 불필요**.
--   - 클라이언트는 이 마이그레이션 **실행 전에도 죽지 않는다**: fetchMyProfile 이 컬럼을
--     명시 나열하지 않고(`select *`) 응답에서 `is_admin ?? false` 로만 읽는다.
--     (없는 컬럼을 select 목록에 넣으면 PostgREST 가 42703 으로 프로필 조회 전체를 실패시킨다.)
--     따라서 이 파일은 배포 순서에 종속되지 않는다.
--
-- 멱등성 (재실행 안전)
-- ---------------------------------------------------------------------------
-- 컬럼은 IF NOT EXISTS, comment 는 덮어쓰기라 몇 번을 다시 실행해도 안전하다.
--
-- ⚠ 원장(append-only): 이전 마이그레이션(20260714… ~ 20260724…)은 절대 수정하지 않는다.
--   이 파일이 그 위에 is_admin 을 덧붙인다.
-- ⚠ shared/lib/supabase/types.ts 와 동기화된다(ProfileRow.is_admin).
-- =============================================================================

-- =============================================================================
-- 1. is_admin 컬럼 — 운영자 여부
-- =============================================================================
alter table public.profiles
  add column if not exists is_admin boolean not null default false;

comment on column public.profiles.is_admin is
  '운영자 여부. true 면 관리자 전용 UI 가 노출된다. 기본 false 이며 사용자는 스스로 바꿀 수 없다(update GRANT 없음) — 지정은 대시보드/SQL 수동. ⚠ RLS 권한과는 무관한 표시 힌트다.';

